package list_Collection_new;

public class List_complexcity_05 {

	public static void main(String[] args) {

		int arr[] = { 1, 2, 3, 4, 5, 6 };

		for (int i = 0; i < arr.length; i++) {
			System.out.println(i);
		}
		
		System.out.println("--------------------");
		
		String trr[] = { "A", "B", "C", "D", "E" };

		for (String f : trr) {
			System.out.println(f);
		}

	}

}
